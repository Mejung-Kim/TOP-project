package kdb.spring.vo;

public class UserVo {
	private int userNo; //NOT NULL
	private String userId; //NOT NULL
	private String userName; //NOT NULL
	private int gender; //NOT NULL
	private String nickname;
	private String profileImg;
	private String intro;
	private String pass; //NOT NULL
	private String phone;
	
	public UserVo(){}
	public UserVo(int userNo, String profileImg){
		this.userNo=userNo;
		this.profileImg=profileImg;
	}
	
	@Override
	public String toString() {
		return "UserVo [userNo=" + userNo + ", userId=" + userId
				+ ", userName=" + userName + ", gender=" + gender
				+ ", nickname=" + nickname + ", profileImg=" + profileImg
				+ ", intro=" + intro + ", pass=" + pass + ", phone=" + phone
				+ "]";
	}
	public int getUserNo() {
		return userNo;
	}
	public void setUserNo(int userNo) {
		this.userNo = userNo;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public int getGender() {
		return gender;
	}
	public void setGender(int gender) {
		this.gender = gender;
	}
	public String getNickname() {
		return nickname;
	}
	public void setNickname(String nickname) {
		this.nickname = nickname;
	}
	
	public String getProfileImg() {
		return profileImg;
	}
	public void setProfileImg(String profileImg) {
		this.profileImg = profileImg;
	}
	public String getIntro() {
		return intro;
	}
	public void setIntro(String intro) {
		this.intro = intro;
	}
	public String getPass() {
		return pass;
	}
	public void setPass(String pass) {
		this.pass = pass;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
}
